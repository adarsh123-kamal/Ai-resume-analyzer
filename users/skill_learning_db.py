def predict_roles(skills):

    predicted_roles = []

    for role, required_skills in ROLE_MAP.items():
        match_count = len(set(skills) & set(required_skills))

        if match_count >= 2:
            predicted_roles.append(role)

    if not predicted_roles:
        predicted_roles.append("Software Developer")

    return predicted_roles